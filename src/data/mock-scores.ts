/**
 * 특정 도시의 12개월 점수 조회
 */
export function getScoresForCity(cityId: string): MonthlyScore[] {
  return mockScores.filter((s) => s.cityId === cityId);
}

/**
 * 특정 월의 전체 도시 점수 조회
 */
export function getScoresForMonth(month: number): MonthlyScore[] {
  return mockScores.filter((s) => s.month === month);
}

/**
 * 특정 도시의 최적 여행 월 (total 최고)
 */
export function getBestMonthForCity(cityId: string): MonthlyScore {
  const cityScores = getScoresForCity(cityId);
  return cityScores.reduce((best, current) =>
    current.scores.total > best.scores.total ? current : best
  );
}

/**
 * 특정 월 기준 상위 도시 조회 (total 내림차순)
 */
export function getTopCitiesForMonth(
  month: number,
  limit: number = 5
): MonthlyScore[] {
  return getScoresForMonth(month)
    .sort((a, b) => b.scores.total - a.scores.total)
    .slice(0, limit);
}

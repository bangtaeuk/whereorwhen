import type { MonthlyScore } from "@/types";

/**
 * 20개 도시 × 12개월 현실적 목업 점수 데이터
 *
 * 점수 기준 (각 1.0 ~ 10.0):
 * - weather: 맑은 날 비율 + 쾌적 기온(15-28°C)
 * - cost: 환율 유리도 + 현지 물가
 * - crowd: 높을수록 한산 (한국 연휴 + 현지 피크 반영)
 * - buzz: 블로그/SNS 언급량 (연간 평균 대비)
 * - total: W1(0.35)×weather + W2(0.25)×cost + W3(0.25)×buzz + W4(0.15)×crowd
 */

function s(
  cityId: string,
  month: number,
  weather: number,
  cost: number,
  crowd: number,
  buzz: number
): MonthlyScore {
  const total =
    Math.round(
      (0.35 * weather + 0.25 * cost + 0.15 * crowd + 0.25 * buzz) * 10
    ) / 10;
  return {
    cityId,
    month,
    scores: { weather, cost, crowd, buzz, total },
  };
}

